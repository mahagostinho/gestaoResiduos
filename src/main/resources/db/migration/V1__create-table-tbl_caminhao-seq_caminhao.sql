CREATE SEQUENCE SEQ_CAMINHAO
  START WITH 1
  INCREMENT BY 1
  NOCACHE
  NOCYCLE;


CREATE TABLE TBL_CAMINHAO (
    CAMINHAO_ID INTEGER DEFAULT SEQ_CAMINHAO.NEXTVAL NOT NULL,
    MODELO VARCHAR2(50) NOT NULL,
    CAPACIDADE_MAXIMA_LITROS NOT NULL,
    STATUS BOOLEAN NOT NULL
);

CREATE SEQUENCE SEQ_RECIPIENTE
  START WITH 1
  INCREMENT BY 1
  NOCACHE
  NOCYCLE;

CREATE TABLE TBL_RECIPIENTE (
    RECIPIENTE_ID INTEGER DEFAULT SEQ_RECIPIENTE.NEXTVAL NOT NULL,
    VOLUME_ATUAL_LITROS NUMBER(6,2) NOT NULL,
    VOLUME_TOTAL_LITROS NUMBER(6,2) NOT NULL,
    STATUS VARCHAR2(20) NOT NULL
);